package core_java.fundamentals_basics.variables_datatypes;

public class Constants {
    public static void main(String[] args) {
        final byte MONTHS_IN_A_YEAR = 12;
        final double PI = 3.14159265359;

        System.out.println("Months in a year: " + MONTHS_IN_A_YEAR);    // Output: Months in a year: 12
        System.out.println("The value of PI: " + PI);                   // Output: The value of PI: 3.14159265359
    }
}